    private void btnConfirmarModificacionActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnConfirmarModificacionActionPerformed
        String ID = txtIdArriendo.getText();
        try {
            int intIdArriendo = Integer.parseInt(ID);
            
            Conexion conector = new Conexion();
            Connection conn = conector.conectar();
            String query = "DELETE FROM ARRIENDO WHERE ID = ?";
            PreparedStatement preparedStmt = conn.prepareStatement(query);
            preparedStmt.setInt(1, intIdArriendo);
            preparedStmt.execute();
            conn.close();
            preparedStmt.close();
            
            
            JOptionPane.showMessageDialog(null, "Acción realizada con exito.");
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "ERROR: INGRESE UN VALOR VALIDO");
            System.out.println("ERROR:" + e.toString());
        }
    }//GEN-LAST:event_btnConfirmarModificacionActionPerformed

