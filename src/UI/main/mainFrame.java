    private void btnUpdateISBNActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUpdateISBNActionPerformed
        try {
            DefaultTableModel tModel = (DefaultTableModel) tblISBN.getModel();
            //Clean table
            tModel.setRowCount(0);
            
            //Query
            String query = "SELECT * FROM ISBN";
            
            Conexion conector = new Conexion();
            Connection conn = conector.conectar();
            
            // create the java statement
            Statement st = conn.createStatement();
            // execute the query, and get a java resultset
            ResultSet rs = st.executeQuery(query);      // iterate through the java resultset
            while (rs.next())
            {
                String ISBN = rs.getString("ISBN");
                String categoria = rs.getString("CATEGORIA");
                String idioma = rs.getString("IDIOMA");
                String autor = rs.getString("AUTOR");
                String editorial = rs.getString("EDITORIAL");
                String titulo = rs.getString("TITULO");
                int numPaginas = rs.getInt("NUMERO_PAGINA");
                Date fechaPublicacion = rs.getDate("FECHA_PUBLICACION");
                
                //Fill the table with data
                Object[] fila = new Object[8];
                fila[0] = ISBN;
                fila[1] = titulo;
                fila[2] = autor;
                fila[3] = idioma;
                fila[4] = editorial;
                fila[5] = categoria;
                fila[6] = numPaginas;
                fila[7] = fechaPublicacion;
                tModel.addRow(fila);;
            }
            st.close();

            JOptionPane.showMessageDialog(null, "Tabla actualizada.");
        } catch (Exception e) {
            System.out.println("------------------");
            System.out.println("Got an Exception");
            System.out.println("ERROR: " + e.toString());
        }
    }//GEN-LAST:event_btnUpdateISBNActionPerformed

